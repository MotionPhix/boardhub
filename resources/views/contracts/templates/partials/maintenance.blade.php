  <h2 class="section-title">3. MAINTENANCE AND LIABILITY</h2>

  <div class="subsection">
    <h3>3.1 Maintenance Responsibilities</h3>
    <p>
      3.1.1 The Company shall maintain the structural integrity of the billboard(s).<br>
      3.1.2 Regular inspections will be conducted to ensure the quality of display is maintained.<br>
      3.1.3 Any lighting or electrical components will be maintained by the Company.<br>
      3.1.4 The Company shall respond to maintenance issues within 48 hours of notification.
    </p>
