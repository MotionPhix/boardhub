  <div class="subsection">
    <h3>3.2 Damage and Repairs</h3>
    <p>
      3.2.1 The Company shall repair any damage caused by weather, vandalism, or normal wear and tear.<br>
      3.2.2 Repairs will be carried out within a reasonable timeframe, weather permitting.<br>
      3.2.3 The Client shall be compensated for any significant display time lost due to damage or repairs.<br>
      3.2.4 Force majeure events shall not constitute a breach of this agreement.
    </p>
  </div>

  <div class="subsection">
    <h3>3.3 Liability and Insurance</h3>
    <p>
      3.3.1 The Company maintains comprehensive insurance coverage for all billboard structures.<br>
      3.3.2 The Client is responsible for insurance of their advertising content and intellectual property.<br>
      3.3.3 The Company shall not be liable for any business losses arising from billboard downtime.<br>
      3.3.4 Both parties shall maintain appropriate public liability insurance.
    </p>
