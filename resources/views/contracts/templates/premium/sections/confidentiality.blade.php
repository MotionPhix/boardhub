<div class="section">
  <h2 class="section-title">6. CONFIDENTIALITY</h2>

  <div class="subsection">
    <h3>6.1 Confidential Information</h3>
    <p>Each party acknowledges that during the Contract Period it may receive Confidential Information from the other party. Each party agrees that:</p>

    <ol type="a" class="ml-4">
      <li>it shall maintain the confidentiality of all Confidential Information;</li>
      <li>it shall use Confidential Information solely for the purposes of this Agreement;</li>
      <li>it shall apply security measures no less stringent than it applies to its own confidential information;</li>
      <li>it shall restrict access to Confidential Information to those employees who need to know for the purposes of this Agreement.</li>
    </ol>
  </div>

  <div class="subsection">
    <h3>6.2 Exceptions</h3>
    <p>The obligations of confidentiality shall not apply to information that:</p>

    <ol type="a" class="ml-4">
      <li>is or becomes publicly known through no fault of the receiving party;</li>
      <li>was in the receiving party's possession before receipt from the disclosing party;</li>
      <li>is rightfully received from a third party without a duty of confidentiality;</li>
      <li>is required to be disclosed by law or governmental authority.</li>
    </ol>
  </div>

  <div class="subsection">
    <h3>6.3 Duration</h3>
    <p>
      6.3.1 The obligations of confidentiality shall survive the termination of this Agreement for a period of five (5) years;<br>
      6.3.2 Upon termination or expiry of this Agreement, each party shall return or destroy all Confidential Information of the other party;<br>
      6.3.3 Any Confidential Information retained for legal requirements shall remain subject to the confidentiality obligations of this Agreement.
    </p>
  </div>
</div>
