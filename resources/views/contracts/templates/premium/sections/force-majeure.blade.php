<div class="section">
  <h2 class="section-title">9. FORCE MAJEURE</h2>

  <div class="subsection">
    <h3>9.1 Force Majeure Events</h3>
    <p>Neither party shall be liable for any failure or delay in performing its obligations under this Agreement due to Force Majeure Events, including but not limited to:</p>

    <ol type="a" class="ml-4">
      <li>Natural disasters (floods, earthquakes, hurricanes);</li>
      <li>Acts of war, terrorism, or civil unrest;</li>
      <li>Government actions or regulations;</li>
      <li>National or regional emergencies;</li>
      <li>Power failures or telecommunications breakdowns;</li>
      <li>Epidemics or pandemics;</li>
      <li>Any other events beyond reasonable control.</li>
    </ol>
  </div>

  <div class="subsection">
    <h3>9.2 Notification and Mitigation</h3>
    <p>
      9.2.1 The affected party shall notify the other party within 48 hours of becoming aware of a Force Majeure Event;<br>
      9.2.2 The notification shall include:<br>
    </p>
    <ol type="a" class="ml-4">
      <li>Nature of the Force Majeure Event;</li>
      <li>Expected duration of the impact;</li>
      <li>Steps being taken to mitigate the impact;</li>
      <li>Regular updates on the situation.</li>
    </ol>
  </div>

