    <h3>4.2 Maintenance Schedule</h3>
    <p>The Company shall conduct regular maintenance as follows:</p>

    <ol type="a" class="ml-4">
      <li>Weekly visual inspections of all advertising displays;</li>
      <li>Monthly technical inspections of lighting and electrical systems;</li>
      <li>Quarterly structural integrity assessments;</li>
      <li>Bi-annual deep cleaning and preventive maintenance;</li>
      <li>Annual comprehensive structural and safety audits.</li>
    </ol>
  </div>

  <div class="subsection">
    <h3>4.3 Quality Assurance</h3>
    <p>
      4.3.1 The Company shall maintain photographic records of display quality;<br>
      4.3.2 Monthly quality reports shall be provided to the Client;<br>
      4.3.3 Independent quality audits shall be conducted quarterly;<br>
      4.3.4 The Client shall be notified of any maintenance that may affect display visibility.
